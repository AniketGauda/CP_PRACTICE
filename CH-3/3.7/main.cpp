// Type Selection Guide for C++

/*
         -- Scenario --                                -- Type --
        Array index, count, small value                 int
        Sum of many numbers, n×m product                long long
        Yes/No answer                                   bool or int (0/1)
        Single letter/digit                             char
        Circle area, geometry                           double
        Modular arithmetic result                       int or long long with % MOD
*/